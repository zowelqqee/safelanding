export function getLegalPathById(id: string): LegalPath | undefined {
  return LEGAL_PATHS.find((path) => path.id === id) ??
    LEGAL_PATHS.find((path) => path.id === LEGAL_PATH_ALIASES[id]);
}

export function getLegalPathsForCountry(countryId: string): LegalPath[] {
  return LEGAL_PATHS.filter((path) => path.countryId === countryId && path.available);
}

export function getAvailablePaths(): LegalPath[] {
  return LEGAL_PATHS.filter((path) => path.available);
}
