def city_count() -> int:
    return len(city_profiles)


def validate_city_profiles() -> None:
    if set(city_profiles) != set(city_id_to_name):
        raise ValueError("city_profiles and city_id_to_name must use the same ids")

    for city_id, profile in city_profiles.items():
        if profile.city_id != city_id:
            raise ValueError(f"City id mismatch for {profile.name}")
        if profile.name != city_id_to_name[city_id]:
            raise ValueError(f"City name mismatch for id {city_id}")
        if profile.region not in CITY_REGIONS:
            raise ValueError(f"Invalid region for {profile.name}: {profile.region}")
        for field_name in CITY_NUMERIC_FIELDS:
            value = getattr(profile, field_name)
            if value != clamp(value):
                raise ValueError(f"{profile.name}.{field_name} must be between 0.0 and 1.0")


validate_city_profiles()
